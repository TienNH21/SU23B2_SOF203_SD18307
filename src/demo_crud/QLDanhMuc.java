    public void loadTable()
    {
        DanhMucDAO cateDao = new DanhMucDAO();
        try {
            ArrayList<DanhMuc> dsDanhMuc = cateDao.findAll();
            DefaultTableModel dtm = (DefaultTableModel) this.tblDanhMuc.getModel();
            for (DanhMuc cate : dsDanhMuc) {
                Object[] row = {
                    cate.getId(),
                    cate.getTen(),
                    cate.getMoTa(),
                    cate.getTrangThai(),
                };

                dtm.addRow(row);
            }
        } catch (Exception e) {
            System.out.println("Kết nối thất bại");
            e.printStackTrace();
        }
    }

